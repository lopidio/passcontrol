    public UserBean writeBeanFromScreen()
    {
        UserBean bean = null;
        // chama a validação
        if( validScreen() )
        {
            // pegando informações da tela
            bean = new UserBean();
            bean.setLogin(tfLoginUser.getText());
            bean.setName(tfNameUser.getText());
            bean.setPassword(new String(tfSenha.getPassword()));
        }
        return bean;
    }
    //==============================================================================
    private boolean validScreen() 
    {
        while(tfNameUser.getText().equals(""))
        {
            tfNameUser.setText(JOptionPane.showInputDialog("Campo nome não pode ser em branco!"));
        }
        if(tfLoginUser.getText().equals(""))
            return false;
        if(tfSenha.getPassword().length == 0)
            return false;
        return true;
    }
