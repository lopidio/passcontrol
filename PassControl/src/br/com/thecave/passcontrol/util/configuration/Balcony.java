package br.com.thecave.passcontrol.util.configuration;

public class Balcony
{
    private String ipBalcony;
    private String mainImage;
    private int lastBalconyNumber;
    private int lastBalconyType;

    /**
     * @return the ipBalcony
     */
    public String getIpBalcony() 
    {
        return ipBalcony;
    }

    /**
     * @param ipBalcony the ipBalcony to set
     */
    public void setIpBalcony(String ipBalcony) 
    {
        this.ipBalcony = ipBalcony;
    }

    /**
     * @return the mainImage
     */
    public String getMainImage() 
    {
        return mainImage;
    }

    /**
     * @param mainImage the mainImage to set
     */
    public void setMainImage(String mainImage) 
    {
        this.mainImage = mainImage;
    }

    /**
     * @return the lastBalconyNumber
     */
    public int getLastBalconyNumber() 
    {
        return lastBalconyNumber;
    }

    /**
     * @param lastBalconyNumber the lastBalconyNumber to set
     */
    public void setLastBalconyNumber(int lastBalconyNumber) 
    {
        this.lastBalconyNumber = lastBalconyNumber;
    }

    /**
     * @return the lastBalconyType
     */
    public int getLastBalconyType() 
    {
        return lastBalconyType;
    }

    /**
     * @param lastBalconyType the lastBalconyType to set
     */
    public void setLastBalconyType(int lastBalconyType) 
    {
        this.lastBalconyType = lastBalconyType;
    }
}
